///////////////////////////////////////////////////////////////////////////////////////////////////
// Construction/Destruction
///////////////////////////////////////////////////////////////////////////////////////////////////

// Default constructor
AuditThreadParameter::AuditThreadParameter()
                     :timeoutAt( PXS_TIME_MAX ),    // No time-out
                      LocalTime(),
                      pWinAuditFrame( nullptr ),
                      Categories()
{
}

// Copy constructor
AuditThreadParameter::AuditThreadParameter( const AuditThreadParameter& oParameter )
{
    AuditThreadParameter();
    *this = oParameter;
}

// Destructor
AuditThreadParameter::~AuditThreadParameter()
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////
// Operators
///////////////////////////////////////////////////////////////////////////////////////////////////

// Assignment operator
AuditThreadParameter& AuditThreadParameter::operator= ( const AuditThreadParameter& oParameter )
{
    if ( this == &oParameter ) return *this;

    timeoutAt      = oParameter.timeoutAt;
    LocalTime      = oParameter.LocalTime;
    pWinAuditFrame = oParameter.pWinAuditFrame;
    Categories     = oParameter.Categories;

    return *this;
}

