// Convenience class to call Mutex::Unlock when the class goes out of scope.
// The caller must ensure the Mutex object is valid while an instance of this
// class is in scope.

///////////////////////////////////////////////////////////////////////////////////////////////////
// Include Files
///////////////////////////////////////////////////////////////////////////////////////////////////

// 1. Own Interface
#include "PxsBase/Header Files/AutoUnlockMutex.h"

// 2. C System Files

// 3. C++ System Files

// 4. Other Libraries

// 5. This Project
#include "PxsBase/Header Files/Exception.h"
#include "PxsBase/Header Files/Mutex.h"

///////////////////////////////////////////////////////////////////////////////////////////////////
// Construction/Destruction
///////////////////////////////////////////////////////////////////////////////////////////////////

// Default constructor
AutoUnlockMutex::AutoUnlockMutex()
                :m_pMutex( nullptr )
{
}

// Parameter Constructor
AutoUnlockMutex::AutoUnlockMutex( Mutex* pMutex )
                :m_pMutex( pMutex )
{
}

// Copy constructor - not allowed so no implementation

// Destructor
AutoUnlockMutex::~AutoUnlockMutex()
{
    try
    {
        Unlock();
    }
    catch ( const Exception& e )
    {
        PXSLogException( e, __FUNCTION__ );
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////
// Operators
///////////////////////////////////////////////////////////////////////////////////////////////////

// Assignment operator - not allowed so no implementation

///////////////////////////////////////////////////////////////////////////////////////////////////
// Public Methods
///////////////////////////////////////////////////////////////////////////////////////////////////

//===============================================================================================//
//  Description:
//      Unlock the mutex, if there is one
//
//  Parameters:
//      None
//
//  Returns:
//      void
//===============================================================================================//
void AutoUnlockMutex::Unlock()
{
    if ( m_pMutex )
    {
        m_pMutex->Unlock();
    }
    m_pMutex = nullptr;
}

