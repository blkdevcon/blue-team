///////////////////////////////////////////////////////////////////////////////////////////////////
// Construction/Destruction
///////////////////////////////////////////////////////////////////////////////////////////////////

// Default constructor
BoundsException::BoundsException()
                :Exception( PXS_ERROR_TYPE_APPLICATION,
                            PXS_ERROR_OUT_OF_BOUNDS,
                            nullptr,
                            nullptr )
{
}

// Constructor with additional details and the throwing method/function
BoundsException::BoundsException( LPCWSTR pszDetails, const char* pszFunction )
                :Exception( PXS_ERROR_TYPE_APPLICATION,
                            PXS_ERROR_OUT_OF_BOUNDS,
                            pszDetails,
                            pszFunction )
{
}

// Copy constructor
BoundsException::BoundsException( const BoundsException& oBounds )
                :Exception( PXS_ERROR_TYPE_APPLICATION,
                            PXS_ERROR_OUT_OF_BOUNDS,
                            nullptr,
                            nullptr )
{
    *this = oBounds;
}

// Destructor - do not throw any exceptions
BoundsException::~BoundsException()
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////
// Operators
///////////////////////////////////////////////////////////////////////////////////////////////////

// Assignment operator
BoundsException& BoundsException::operator= ( const BoundsException& oBounds )
{
    if ( this == &oBounds ) return *this;

    // Base class
    Exception::operator= ( oBounds );

    m_uErrorCode  = oBounds.m_uErrorCode;
    m_uErrorType  = oBounds.m_uErrorType;
    m_CrashString = oBounds.m_CrashString;
    m_Description = oBounds.m_Description;
    m_Details     = oBounds.m_Details;
    m_Message     = oBounds.m_Message;

    return *this;
}

