///////////////////////////////////////////////////////////////////////////////////////////////////
// Construction/Destruction
///////////////////////////////////////////////////////////////////////////////////////////////////


// Default constructor
NullException::NullException()
              :Exception( PXS_ERROR_TYPE_APPLICATION,
                          PXS_ERROR_NULL_POINTER,
                          nullptr,
                          nullptr )
{
}


// Constructor with additional details and the throwing method/function
NullException::NullException( LPCWSTR pszDetails, const char* pszFunction )
              :Exception( PXS_ERROR_TYPE_APPLICATION,
                          PXS_ERROR_NULL_POINTER,
                          pszDetails,
                          pszFunction )
{
}

// Copy constructor
NullException::NullException( const NullException& oExceptionNull )
              :Exception( PXS_ERROR_TYPE_APPLICATION,
                          PXS_ERROR_NULL_POINTER,
                          nullptr,
                          nullptr )
{
    *this = oExceptionNull;
}

// Destructor
NullException::~NullException()
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////
// Operators
///////////////////////////////////////////////////////////////////////////////////////////////////

// Assignment operator
NullException& NullException::operator= ( const NullException& oNull )
{
    if ( this == &oNull ) return *this;

    // Base class
    Exception::operator= ( oNull );

    m_uErrorCode  = oNull.m_uErrorCode;
    m_uErrorType  = oNull.m_uErrorType;
    m_CrashString = oNull.m_CrashString;
    m_Description = oNull.m_Description;
    m_Details     = oNull.m_Details;
    m_Message     = oNull.m_Message;

    return *this;
}

