{
}

// Parameter Constructor
AutoVariantClear::AutoVariantClear( VARIANT* pVariant )
                 :m_pVariant( pVariant )
{
}

// Copy constructor - not allowed so no implementation

// Destructor
AutoVariantClear::~AutoVariantClear()
{
    if ( m_pVariant )
    {
        VariantClear( m_pVariant );
    }
}

