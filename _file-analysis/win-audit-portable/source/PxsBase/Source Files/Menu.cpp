///////////////////////////////////////////////////////////////////////////////////////////////////
// Construction/Destruction
///////////////////////////////////////////////////////////////////////////////////////////////////

// Default constructor
Menu::Menu()
     :m_hMenu( nullptr ),
      m_menuItemInfo()    // Non-op
{
    // Init the structure
    memset( &m_menuItemInfo, 0, sizeof ( m_menuItemInfo ) );
    m_menuItemInfo.cbSize = sizeof ( m_menuItemInfo );
}

// Copy constructor
Menu::Menu( const Menu& oMenu )
     :m_hMenu( nullptr ),
      m_menuItemInfo()    // Non-op
{
    // Init the structure
    memset( &m_menuItemInfo, 0, sizeof ( m_menuItemInfo ) );
    m_menuItemInfo.cbSize = sizeof ( m_menuItemInfo );

    *this = oMenu;
}

// Destructor
Menu::~Menu()
{
    // Clean up
    if ( m_hMenu )
    {
        DestroyMenu( m_hMenu );
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////
// Operators
///////////////////////////////////////////////////////////////////////////////////////////////////

// Assignment operator
Menu& Menu::operator= ( const Menu& oMenu )
{
    if ( this == &oMenu) return *this;

    m_hMenu = oMenu.m_hMenu;
    memcpy( &m_menuItemInfo, &oMenu.m_menuItemInfo, sizeof ( m_menuItemInfo ) );

    return *this;
}

