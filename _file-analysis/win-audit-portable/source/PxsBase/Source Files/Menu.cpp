//===============================================================================================//
//  Description:
//      Get the handle of the menu or menu item
//
//  Parameters:
//      None
//
//  Returns:
//      HMENU
//===============================================================================================//
HMENU Menu::GetMenuHandle() const
{
    return m_hMenu;
}

