///////////////////////////////////////////////////////////////////////////////////////////////////
// Construction/Destruction
///////////////////////////////////////////////////////////////////////////////////////////////////

// Default constructor
SocketException::SocketException()
                :Exception( PXS_ERROR_TYPE_SYSTEM,
                            0,
                            nullptr,
                            nullptr )
{
}

// Constructor with a int Windows socket error, details message and 
// throwing method/function
SocketException::SocketException( int errorCode, LPCWSTR pszDetails, const char* pszFunction )
                :Exception( PXS_ERROR_TYPE_SYSTEM,
                            static_cast<DWORD>(errorCode),
                            pszDetails,
                            pszFunction )
{
}

// Copy constructor
SocketException::SocketException( const SocketException& oSocket )
                :Exception( PXS_ERROR_TYPE_SYSTEM,
                            0,
                            nullptr,
                            nullptr )
{
    *this = oSocket;
}

// Destructor
SocketException::~SocketException()
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////
// Operators
///////////////////////////////////////////////////////////////////////////////////////////////////

// Assignment operator
SocketException& SocketException::operator= ( const SocketException& oSocket )
{
    // Check for self-assignment
    if ( this == &oSocket ) return *this;

    m_uErrorCode  = oSocket.m_uErrorCode;
    m_uErrorType  = oSocket.m_uErrorType;
    m_CrashString = oSocket.m_CrashString;
    m_Description = oSocket.m_Description;
    m_Details     = oSocket.m_Details;
    m_Message     = oSocket.m_Message;

    return *this;
}

